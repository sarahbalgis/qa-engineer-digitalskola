package com.learnProgramming.advance;

public class Class1 {

    int number;
    String name;
    String club;

    public Class1(int number, String name, String club) {
        this.number = number;
        this.name = name;
        this.club = club;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClub() {
        return club;
    }

    public void setClub(String club) {
        this.club = club;
    }


}
